void	skip_quote(char *res, char *s, int *x, int *y)
{
	int	i;
	int	j;

	i = *x;
	j = *y;
	res[j++] = s[i++];
	while (s[i] != '\'')
		res[j++] = s[i++];
	res[j++] = s[i++];
	*x = i;
	*y = j;
}
